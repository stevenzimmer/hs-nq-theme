var searchTime;
$('.table-input').bind('keyup', function () {
  $('.nQueryPacks-card-icon').show();
  $('.nQueryPacks-no-results').hide();
  $('.nQueryPacks-search .fas').removeClass('fa-search').addClass('fa-dot-circle');
  clearTimeout(searchTime);

  searchTime = setTimeout(function () {
      var $val = $('.table-input').val().trim();
      if ($val.length !== 0) {
          $('.nQueryPacks-cards .nQueryPacks-card')
              .show()
              .hide()
              .each(function () {
                  if (
                      $(this)
                          .find('.nQueryPacks-card-title')
                          .is(':icontains(' + $val + ')')
                  ) {
                      $(this).show();
                  }
              });
      } else {
          $('.nQueryPacks-cards .nQueryPacks-card')
              .show()
              .hide()
              .each(function () {
                  $(this).show();
              });
      }
      $('.nQueryPacks-search .fas').addClass('fa-search').removeClass('fa-dot-circle');
      var visibles = 0;
      $('.nQueryPacks-cards .nQueryPacks-card').each(function () {
          if ($(this).is(':visible')) {
              visibles++;
          }
      });
      if (visibles == 0) {
          $('.nQueryPacks-no-results').show();
      }
      if (visibles == tables.length) {
          $('.search-results-showing').html('All nQuery tables are shown below. Search to filter.');
      } else {
          $('.search-results-showing').html(visibles + ' nQuery tables match your description. <strong class="cursor-pointer underline text-nq-jade">Click to reset filters.</strong>');
      }

      if ($val.length !== 0) {
          try {
              var xhr = new XMLHttpRequest();
              xhr.addEventListener('readystatechange', function () {
                  if (this.readyState === 4) {
                      console.log(this.responseText);
                  }
              });
              xhr.open('POST', 'https://hooks.zapier.com/hooks/catch/13455769/bckh1cw/');
              xhr.send(
                  JSON.stringify({
                      search: $val,
                      results: visibles
                  })
              );
          } catch (e) {
              console.warn('Error seding analytics data to API');
              console.warn(e);
          }
      }
  }, 500);
});

$.expr[':'].icontains = function (obj, index, meta, stack) {
  return (obj.textContent || obj.innerText || $(obj).text() || '').toLowerCase().indexOf(meta[3].toLowerCase()) >= 0;
};

